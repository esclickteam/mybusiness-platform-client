export function getPageSections(
  page: VisualLibraryPageTemplate,
): VisualLibrarySectionTemplate[] {
  return page.sectionIds
    .map((sectionId) =>
      SECTION_LIBRARY.find((section) => section.id === sectionId),
    )
    .filter(
      (
        section,
      ): section is VisualLibrarySectionTemplate => Boolean(section),
    );
}
