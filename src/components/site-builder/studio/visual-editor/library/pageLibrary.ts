export function getPagesByCategory(
  cat: string,
): VisualLibraryPageTemplate[] {
  if (!cat || cat === "all") {
    return PAGE_LIBRARY;
  }
  return PAGE_LIBRARY.filter((page) => page.category === cat);
}

