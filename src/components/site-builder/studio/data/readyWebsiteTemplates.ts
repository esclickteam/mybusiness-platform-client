function withBlockIds(
  templateId: string,
  blocks: TemplateBlockInput[]
): ReadyWebsiteBlock[] {
  return blocks.map((block, index) => ({
    ...block,
    id: `${templateId}-${index + 1}`,
  }));
}

function makeSeed(input: NicheTemplateInput): ReadyWebsiteTemplateSeed {
  return {
    ...input,
    blocks: withBlockIds(input.id, input.blocks),
  };
}

