export default function ProfileFAQ() {
  const [openIndex, setOpenIndex] = useState(null);

  const toggle = (index) => {
    setOpenIndex(openIndex === index ? null : index);
  };

  return (
    <div style={{ maxWidth: 700, margin: "auto", fontFamily: "Arial, sans-serif" }}>
      <h1 style={{ textAlign: "center", marginBottom: 20 }}>שאלות ותשובות - פרופיל העסק</h1>
      {faqData.map((item, index) => (
        <div
          key={index}
          style={{ marginBottom: 15, borderBottom: "1px solid #ddd", paddingBottom: 10 }}
        >
          <button
            onClick={() => toggle(index)}
            style={{
              width: "100%",
              textAlign: "right",
              background: "#f3f3f3",
              border: "none",
              padding: "10px 15px",
              fontSize: 18,
              fontWeight: "bold",
              cursor: "pointer",
              outline: "none",
            }}
            aria-expanded={openIndex === index}
            aria-controls={`faq-answer-${index}`}
          >
            {item.question}
          </button>
          {openIndex === index && (
            <div
              id={`faq-answer-${index}`}
              style={{
                padding: "10px 15px",
                background: "#fafafa",
                whiteSpace: "pre-line",
                textAlign: "right",
                fontSize: 16,
              }}
            >
              {item.answer}
            </div>
          )}
        </div>
      ))}
    </div>
  );
}
