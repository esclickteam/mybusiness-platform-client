// src/pages/Login.jsx
import React, { useState } from "react";
import { useAuth } from "../context/AuthContext";
import "../styles/Login.css";
import ForgotPassword from "./ForgotPassword";
import { Link, useNavigate } from "react-router-dom";

export default function Login() {
  const { login, logout, error: contextError } = useAuth(); 
  const [identifier, setIdentifier] = useState("");  //  转   砖 砖转砖
  const [password, setPassword] = useState("");
  const [loading, setLoading]   = useState(false);
  const [loginError, setLoginError] = useState("");
  const [showForgot, setShowForgot] = useState(false);
  const navigate = useNavigate();

  const handleSubmit = async (e) => {
    e.preventDefault();
    setLoginError("");

    if (!identifier.trim() || !password) {
      setLoginError("砖  /砖 砖转砖 住住");
      return;
    }

    setLoading(true);
    try {
      const user = await login(
        identifier.trim(),
        password,
        { skipRedirect: true }
      );

      if (user.role === "business") {
        navigate(`/business/${user.businessId}/dashboard`, { replace: true });
      } else if (user.role === "customer") {
        navigate("/client/dashboard", { replace: true });
      } else {
        await logout();
        setLoginError("  专砖 转专 ");
      }
    } catch (err) {
      console.error("Login failed:", err);
      setLoginError(contextError || err.response?.data?.error || "砖 转专转");
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="login-container">
      <div className="login-box">
        <h2>转专转</h2>
        <form onSubmit={handleSubmit}>
          <input
            type="text"
            name="identifier"
            placeholder="  砖 砖转砖"
            value={identifier}
            onChange={(e) => setIdentifier(e.target.value)}
            disabled={loading}
            required
          />
          <input
            type="password"
            name="password"
            placeholder="住住"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            disabled={loading}
            required
          />

          <button type="submit" className="login-button" disabled={loading}>
            {loading ? " 转专..." : "转专"}
          </button>
        </form>

        {(loginError || contextError) && (
          <p className="error-message">{loginError || contextError}</p>
        )}

        <div className="login-extra-options">
          <span
            className="forgot-password"
            onClick={() => setShowForgot(true)}
          >
            砖转 转 住住?
          </span>

          <p className="signup-link">
             专砖?{" "}
            <Link to="/register" className="signup-link-text">
              专砖 注砖
            </Link>
          </p>

          <button
            className="staff-login-btn"
            onClick={() => navigate("/staff-login")}
            disabled={loading}
          >
            住转 注
          </button>
        </div>
      </div>

      {showForgot && <ForgotPassword closePopup={() => setShowForgot(false)} />}
    </div>
  );
}
